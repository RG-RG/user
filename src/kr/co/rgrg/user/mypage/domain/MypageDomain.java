package kr.co.rgrg.user.mypage.domain;

public class MypageDomain {
	
	private String nickname, blog_name, alarm_agree, github, website, visible_email, profile, statement;

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getBlog_name() {
		return blog_name;
	}

	public void setBlog_name(String blog_name) {
		this.blog_name = blog_name;
	}

	public String getAlarm_agree() {
		return alarm_agree;
	}

	public void setAlarm_agree(String alarm_agree) {
		this.alarm_agree = alarm_agree;
	}

	public String getGithub() {
		return github;
	}

	public void setGithub(String github) {
		this.github = github;
	}

	public String getWebsite() {
		return website;
	}

	public void setWebsite(String website) {
		this.website = website;
	}

	public String getVisible_email() {
		return visible_email;
	}

	public void setVisible_email(String visible_email) {
		this.visible_email = visible_email;
	}

	public String getProfile() {
		return profile;
	}

	public void setProfile(String profile) {
		this.profile = profile;
	}

	public String getStatement() {
		return statement;
	}

	public void setStatement(String statement) {
		this.statement = statement;
	}
	
	
}
